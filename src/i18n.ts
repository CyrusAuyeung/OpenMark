export const translations: Record<AppLocale, TranslationCatalog> = {
  en,
  'zh-CN': zhCN,
}

export function isLocalePreference(value: string | null): value is LocalePreference {
  return localePreferenceValues.includes(value as LocalePreference)
}

export function getPreferredLocale(languageCodes: readonly string[]): AppLocale {
  return languageCodes.some((languageCode) => languageCode.toLowerCase().startsWith('zh'))
    ? 'zh-CN'
    : 'en'
}